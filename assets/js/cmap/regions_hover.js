    setInterval(function(){
        var $bretagne = $(".bretagne");
        var $idbretagne = $('#bretagne');
        
        if($bretagne.is(":hover") || $idbretagne.is(":hover")) {
            $bretagne.css("color", "#ADCE4A");
            $bretagne.css("transition", "1s");
            $idbretagne.css("fill", "#ADCE4B");
            $idbretagne.css("transition", "1s");
        } else {
            $bretagne.css("color", "");
            $idbretagne.css("fill", "");
        }
    })

